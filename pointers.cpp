#include "bits/stdc++.h"

using namespace std;

auto main() -> int
{

	int a	   = 10;
	int* ptr_a = &a;

	cout << "a = " << a << endl;
	cout << "ptr_a = " << ptr_a << endl;

	/*
		We often display pointers as hexadecimal numbers because it is more compact than binary or
	   decimal and in many architectures facts about the address are easy to extract (such as
	   alignment - whether it has a remainder divided by another number invariably a power of 2).
	*/
}